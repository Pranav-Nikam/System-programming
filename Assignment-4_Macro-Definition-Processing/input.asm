MACRO
SAMPLE &X, &N, &REG=AREG, &REG1=BRAG
LCL &M
&M SET 0
MOVER &REG, ="0"
.MORE MOVEM &REG, &X + &M
.AGAIN &M SET &M + 1
AIF (&M NE &N) .MORE
MEND

MACRO
CALC &Z, &W, &REG2=CREG, &REG3=DREG
LCL &T
.LABEL &T SET &Z
ADD &REG2, &W
.LOOP MOVEM &REG3, &Z
MEND

MACRO
